  /// Validates an Iranian 10-digit postal code.
  ///
  /// Basic checks (always performed):
  /// • Exactly 10 digits after cleaning (spaces and dashes removed)
  /// • Does not start with '0'
  ///
  /// Strict mode (when `strict: true`):
  /// • First 5 digits must fall within one of the known valid ranges
  ///   defined in [iranPostal5DigitRanges]
  /// • Supports both range formats ("xxxxx تا yyyyy") and exact codes ("xxxxx")
  ///
  /// Examples:
  ///   postCode("5133156789")                  → true  (basic)
  ///   postCode(" 51331-56789 ")               → true  (cleaned)
  ///   postCode("0133156789")                  → false (starts with 0)
  ///   postCode("9999999999")                  → true  (basic) / false (strict)
  ///   postCode("5133156789", strict: true)    → true  (Tabriz range)
  ///   postCode("3712345678", strict: true)    → true  (Qom)
  ///   postCode("1234567890", strict: true)    → false (invalid range)
  ///
  /// Recommended usage:
  /// ```dart
  /// // Quick format check
  /// if (!Valersi.postCode(userInput)) { ... invalid ... }
  ///
  /// // Strict check (recommended for forms / registration)
  /// if (!Valersi.postCode(userInput, strict: true)) { ... invalid ... }
  /// ```
  ///
  /// Returns `true` if the postal code passes all applied checks.
  static bool postCode(String input, {bool strict = false}) {
    // Normalize: remove spaces and dashes
    final clean = input.trim().replaceAll(RegExp(r'[\s\-]'), '');

    // Must be exactly 10 digits
    if (clean.length != 10) return false;

    // Only digits allowed
    if (!RegExp(r'^\d{10}$').hasMatch(clean)) return false;

    // Iranian postal codes never start with 0
    if (clean.startsWith('0')) return false;

    // If not in strict mode → basic validation is enough
    if (!strict) return true;

    // Strict mode: check first 5 digits against known ranges
    final prefix = clean.substring(0, 5);
    final prefixNum = int.tryParse(prefix) ?? -1;

    if (prefixNum == -1) return false;

    // Search through all provinces and cities
    for (final provinceRanges in iranPostal5DigitRanges.values) {
      for (final rangeStr in provinceRanges.values) {
        final parts = rangeStr.split(' تا ');

        if (parts.length == 2) {
          // Range: "xxxxx تا yyyyy"
          final min = int.tryParse(parts[0]) ?? 0;
          final max = int.tryParse(parts[1]) ?? 0;

          if (prefixNum >= min && prefixNum <= max) {
            return true;
          }
        } else if (RegExp(r'^\d{5}$').hasMatch(rangeStr)) {
          // Exact match: "xxxxx"
          final exact = int.tryParse(rangeStr) ?? 0;
          if (prefixNum == exact) return true;
        }
      }
    }

    // No matching range found
    return false;
  }
